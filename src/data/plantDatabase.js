function escapeSvg(value) {
  return String(value)
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&apos;');
}

function createSvgDataUri(svg) {
  return `data:image/svg+xml;charset=UTF-8,${encodeURIComponent(svg)}`;
}

function createMonogram(name) {
  return name
    .split(/\s+/)
    .slice(0, 2)
    .map((part) => part.charAt(0).toUpperCase())
    .join('');
}

function buildPlantImage(plant) {
  const palette = IMAGE_PALETTES[plant.type] || IMAGE_PALETTES.other;
  const monogram = createMonogram(plant.commonName);
  const safeName = escapeSvg(plant.commonName);
  const safeScientific = escapeSvg(plant.scientificName);

  return createSvgDataUri(`
    <svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 640 480" role="img" aria-label="${safeName}">
      <defs>
        <linearGradient id="bg" x1="0" y1="0" x2="1" y2="1">
          <stop offset="0%" stop-color="${palette.start}" />
          <stop offset="100%" stop-color="${palette.end}" />
        </linearGradient>
      </defs>
      <rect width="640" height="480" rx="36" fill="url(#bg)" />
      <circle cx="504" cy="112" r="72" fill="rgba(255,255,255,0.2)" />
      <ellipse cx="184" cy="318" rx="118" ry="146" fill="rgba(255,255,255,0.18)" transform="rotate(-18 184 318)" />
      <ellipse cx="326" cy="282" rx="112" ry="154" fill="rgba(255,255,255,0.14)" transform="rotate(14 326 282)" />
      <path d="M134 358c68-82 142-122 222-118-28 62-74 106-144 144-34 18-64 28-90 34 4-21 8-40 12-60z" fill="${palette.accent}" opacity="0.82" />
      <path d="M396 142c54 48 90 122 104 210-46-16-86-40-118-76-36-40-56-88-68-144 32-2 60 2 82 10z" fill="${palette.accent}" opacity="0.58" />
      <rect x="38" y="332" width="220" height="112" rx="28" fill="rgba(255,255,255,0.82)" />
      <text x="68" y="404" font-family="Manrope, Arial, sans-serif" font-size="56" font-weight="700" fill="${palette.accent}">${monogram}</text>
      <text x="40" y="64" font-family="Fraunces, Georgia, serif" font-size="44" font-weight="700" fill="#173320">${safeName}</text>
      <text x="40" y="110" font-family="Manrope, Arial, sans-serif" font-size="24" fill="#254233">${safeScientific}</text>
    </svg>
  `);
}

